/* ── DETAIL PAGE LOGIC ── */
let currentDetailIndex = 0;

function buildDetailHTML(data) {
  const facts = data.facts
    .map(
      (f) =>
        `<div class="detail-fact"><span class="detail-fact-label">${f.label}</span><div class="detail-fact-value">${f.value}</div></div>`,
    )
    .join("");

  const meta = data.meta
    .map(
      (m) =>
        `<div class="detail-meta-tag"><span class="meta-icon">${m.icon}</span><div><strong>${m.label}</strong>&nbsp; ${m.value}</div></div>`,
    )
    .join("");

  const paras = data.about
    .map((p) => `<p class="detail-text">${p}</p>`)
    .join("");

  return `
    <div class="detail-hero">
      <div class="detail-hero-bg" style="background-image:url('${data.bg}')"></div>
      <div class="detail-hero-overlay"></div>
      <div class="detail-hero-content">
        <span class="detail-icon-large">${data.icon}</span>
        <h1 class="detail-title">${data.name}</h1>
        <p class="detail-subtitle">${data.subtitle}</p>
      </div>
    </div>
    <div class="detail-body">
      <div class="detail-meta-row">${meta}</div>
      <div class="detail-section">
        <h2 class="detail-section-title">About <em>This Attraction</em></h2>
        ${paras}
      </div>
      <div class="detail-section">
        <h2 class="detail-section-title">Quick <em>Facts</em></h2>
        <div class="detail-facts">${facts}</div>
      </div>
      <div class="detail-section">
        <h2 class="detail-section-title">Visitor <em>Tip</em></h2>
        <div class="detail-tip-box"><strong>💡 Pro Tip</strong><p>${data.tip}</p></div>
      </div>
      <a href="${data.link}" target="_blank" rel="noopener" class="detail-ext-link">${data.linkText} ↗</a>
    </div>
  `;
}

function openDetail(index) {
  currentDetailIndex = index;
  const overlay = document.getElementById("detailOverlay");
  const data = ATTRACTIONS[lang][index];
  document.getElementById("detailContent").innerHTML = buildDetailHTML(data);
  document.getElementById("detailBreadcrumb").textContent = data.name;
  updateDetailNav();
  overlay.classList.add("open");
  document.body.style.overflow = "hidden";
  overlay.scrollTop = 0;
  // push hash so back button works
  history.pushState({ detail: index }, "", "#attraction-" + index);
}

function closeDetail() {
  document.getElementById("detailOverlay").classList.remove("open");
  document.body.style.overflow = "";
  if (location.hash.startsWith("#attraction-")) {
    history.pushState({}, "", location.pathname);
  }
}

function navigateDetail(dir) {
  const total = ATTRACTIONS[lang].length;
  currentDetailIndex = (currentDetailIndex + dir + total) % total;
  const data = ATTRACTIONS[lang][currentDetailIndex];
  const content = document.getElementById("detailContent");
  content.style.opacity = "0";
  setTimeout(() => {
    content.innerHTML = buildDetailHTML(data);
    document.getElementById("detailBreadcrumb").textContent = data.name;
    content.style.opacity = "1";
    updateDetailNav();
    document.getElementById("detailOverlay").scrollTop = 0;
    history.replaceState(
      { detail: currentDetailIndex },
      "",
      "#attraction-" + currentDetailIndex,
    );
  }, 180);
}

function updateDetailNav() {
  // always enable both (circular navigation)
  document.getElementById("detailPrev").disabled = false;
  document.getElementById("detailNext").disabled = false;
}

// Transition for navigate
document.addEventListener("DOMContentLoaded", () => {
  const c = document.getElementById("detailContent");
  if (c) c.style.transition = "opacity 0.18s ease";
});

// Handle browser back button
window.addEventListener("popstate", (e) => {
  if (e.state && e.state.detail !== undefined) {
    openDetail(e.state.detail);
  } else {
    closeDetail();
  }
});

// Close on ESC
document.addEventListener("keydown", (e) => {
  if (e.key === "Escape") closeDetail();
  if (e.key === "ArrowLeft") {
    if (document.getElementById("detailOverlay").classList.contains("open"))
      navigateDetail(-1);
  }
  if (e.key === "ArrowRight") {
    if (document.getElementById("detailOverlay").classList.contains("open"))
      navigateDetail(1);
  }
});

