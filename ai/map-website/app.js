function cnt(el, n, s = "") {
  let i = 0;
  const t = setInterval(() => {
    i += n / 60;
    if (i >= n) {
      el.textContent = n + s;
      clearInterval(t);
    } else el.textContent = Math.floor(i) + s;
  }, 20);
}
