/* ── LANG ENGINE ── */
let lang = localStorage.getItem("bcp_lang") || "en";

function setLang(l) {
  if (l === lang) return;
  lang = l;
  localStorage.setItem("bcp_lang", lang);
  renderLang();
  syncPills();
}

function renderLang() {
  document.documentElement.lang = lang;
  document.title =
    lang === "ka"
      ? "ბათუმის ცენტრალური პარკი — 6 მაისის პარკი"
      : "Batumi Central Park — 6 May Park";
  const els = document.querySelectorAll("[data-i18n]");
  els.forEach((el) => el.classList.add("lang-fade", "fading"));
  setTimeout(() => {
    els.forEach((el) => {
      const v = T[lang][el.getAttribute("data-i18n")];
      if (v !== undefined) el.innerHTML = v;
      el.classList.remove("fading");
    });
  }, 200);
}

function syncPills() {
  ["swDesktop", "swMobile"].forEach((id) => {
    const sw = document.getElementById(id);
    if (!sw) return;
    const pill = sw.querySelector(".lang-pill");
    sw.querySelectorAll(".lang-btn").forEach((b) =>
      b.classList.toggle("active", b.dataset.lang === lang),
    );
    const active = sw.querySelector(`.lang-btn[data-lang="${lang}"]`);
    if (active && pill) {
      pill.style.width = active.offsetWidth + "px";
      pill.style.transform = `translateX(${active.offsetLeft - 3}px)`;
    }
  });
}

